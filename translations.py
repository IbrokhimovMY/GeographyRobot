def get_country_name(uz_name: str, lang: str) -> str:
    if lang == 'ru':
        return COUNTRY_NAMES_RU.get(uz_name, uz_name)
    if lang == 'en':
        return COUNTRY_NAMES_EN.get(uz_name, uz_name)
    return uz_name


